#Generador de Nombre de Usuario: 
# Solicita al usuario su nombre, apellido y año de nacimiento, 
# y genera un nombre de usuario combinándolos 
# (por ejemplo, "AnaSmith1990").
nombre = str(input("Ingrese su nombre "))
apellido = str(input("Ingrese su apellidos "))
añoNacimiento = str(input("Ingrese su año de nacimiento "))

print(nombre+apellido+añoNacimiento)