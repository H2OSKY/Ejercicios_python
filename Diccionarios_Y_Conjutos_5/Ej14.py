# Unión e Intersección de Conjuntos: 
# Dados dos conjuntos, encuentra su unión e intersección.

# Definir dos conjuntos
conjunto1 = {1, 2, 3, 4, 5}
conjunto2 = {4, 5, 6, 7, 8}

# Calcular la unión de los conjuntos
union = conjunto1.union(conjunto2)

# Calcular la intersección de los conjuntos
interseccion = conjunto1.intersection(conjunto2)

# Mostrar resultados
print(f"Conjunto 1: {conjunto1}")
print(f"Conjunto 2: {conjunto2}")
print(f"Unión: {union}")
print(f"Intersección: {interseccion}")
